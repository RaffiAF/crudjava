    private void BtnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTambahActionPerformed
        // TODO add your handling code here:
        String nama = t_nama.getText();
        String usia = t_usia.getText();
        String berat = t_berat.getText();
        String tinggi = t_tinggi.getText();
        String jenis_kelamin =t_jk.getText();
        String tingkat_aktivitas = t_ta.getText();
        String Kebutuhan_kalori = t_kebutuhan.getText();
        String protein = t_protein.getText();
        String Karbohidrat = t_karbohidrat.getText();
        String Lemak = t_lemak.getText();       
        
        if (nama.isEmpty() || usia.isEmpty() || berat.isEmpty() || tinggi.isEmpty() || jenis_kelamin.isEmpty() || tingkat_aktivitas.isEmpty() || Kebutuhan_kalori.isEmpty() || protein.isEmpty() || Karbohidrat.isEmpty() || Lemak.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua Kolom Harus di isi", "Validasi", JOptionPane.ERROR_MESSAGE);
        return;
        }
        try {
            String sql = "INSERT INTO rencana_diet(nama, usia, berat, tinggi, jenis_kelamin, tingkat_aktivitas, kalori_harian, protein, karbohidrat, lemak) VALUES (?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, nama);
            st.setString(2, usia);
            st.setString(3, berat);
            st.setString(4, tinggi);
            st.setString(5, jenis_kelamin);
            st.setString(6, tingkat_aktivitas);
            st.setString(7, Kebutuhan_kalori);
            st.setString(8, protein);
            st.setString(9, Karbohidrat);
            st.setString(10, Lemak);
            
            int rowinserted = st.executeUpdate();
            if (rowinserted > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil di tambahkan");
                resetForm();
                getData();
            }
            st.close();
        } catch (Exception e) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_BtnTambahActionPerformed

