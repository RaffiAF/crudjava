     private void getData() {
        DefaultTableModel model = (DefaultTableModel) TabelDiet.getModel();
        model.setRowCount(0);
        
         try {
             String sql ="Select * from rencana_diet";
             PreparedStatement st = conn.prepareStatement(sql);
             ResultSet rs = st.executeQuery();
             
             while(rs.next()){
                 int id = rs.getInt("id");
                 String nama = rs.getString("nama");
                 int usia = rs.getInt("usia");
                 double berat = rs.getDouble("berat");
                 double tinggi = rs.getDouble("tinggi");
                 String tingkat_aktivitas = rs.getString("tingkat_aktivitas");
                 String jenis_kelamin = rs.getString("jenis_kelamin");
                 int kalori_harian = rs.getInt("kalori_harian");
                 int protein = rs.getInt("protein");
                 int karbohidrat = rs.getInt("karbohidrat");
                 int lemak = rs.getInt("lemak");
                 
                 Object[] rowData ={id, nama, usia, berat, tinggi, tingkat_aktivitas, jenis_kelamin, kalori_harian, protein, karbohidrat, lemak};
                 model.addRow(rowData);
             }
             
             rs.close();
             st.close();
             
         } catch (Exception e) {
             Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, e);
         }
    }

